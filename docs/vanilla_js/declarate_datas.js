//let declaration
let myName = "홍길동"; // 처음 선언할때 myName에 홍길동 선언
console.log(myName);
myName = "박찬호"; // myName에 박찬호 선언
myName = 5;
console.log();

//const declaration
const nickName = "홍길동"; // 처음 선언할때 nickName에 홍길동 선언. const는 상수라 한 번 선언되면 쉽게 바뀌지 않는다.
console.log(nickName);
nickName = "박찬호"; // 에러. const는 값을 쉽게 바꿀 수 없음.
console.log();
